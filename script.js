// 添加识别的单词到单词库
function addRecognizedWord() {
    const recognitionResult = document.getElementById('recognition-result');
    
    // 检查识别结果是否存在
    if (recognitionResult.innerHTML === '') {
        alert('请先拍照识别单词');
        return;
    }
    
    // 直接从DOM中获取单词和释义
    const paragraphs = recognitionResult.querySelectorAll('p');
    if (paragraphs.length < 2) {
        alert('识别结果格式不正确');
        return;
    }
    
    // 提取单词和释义
    const wordText = paragraphs[0].textContent;
    const meaningText = paragraphs[1].textContent;
    
    const word = wordText.replace('单词: ', '').trim();
    const meaning = meaningText.replace('释义: ', '').trim();
    
    if (word && meaning) {
        const newWord = {
            word: word,
            meaning: meaning,
            example: `I like ${word}.`,
            phonetic: '[' + word.split('').join('-') + ']'
        };
        
        addWordToLibrary(currentWordLibrary, newWord);
        
        // 重置界面
        document.getElementById('add-word-btn').disabled = true;
        recognitionResult.innerHTML = '';
    } else {
        alert('识别结果不完整');
    }
}

