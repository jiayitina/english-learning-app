function startTomatoSession() {
    const groupSize = Math.ceil(current5330Words.length / 3);
    let currentGroup = 0;
    let repetition = 0;
    
    function startGroup() {
        if (currentGroup >= 3) {
            // 所有组复习完成
            return;
        }
        
        const start = currentGroup * groupSize;
        const end = Math.min(start + groupSize, current5330Words.length);
        const groupWords = current5330Words.slice(start, end);
        
        // 显示当前组单词
        _5330Words.innerHTML = '';
        groupWords.forEach(word => {
            const wordCard = document.createElement('div');
            wordCard.className = 'word-card-5330';
            wordCard.innerHTML = `
                <div class="word">${word.word}</div>
                <div class="meaning">${word.meaning}</div>
            `;
            
            // 点击单词卡显示/隐藏中文
            wordCard.addEventListener('click', () => {
                wordCard.classList.toggle('show-meaning');
            });
            
            _5330Words.appendChild(wordCard);
        });
        
        // 重复3遍
        repetition++;
        if (repetition < 3) {
            setTimeout(startGroup, 2000); // 2秒后重复
        } else {
            repetition = 0;
            currentGroup++;
            setTimeout(startGroup, 3000); // 3秒后下一组
        }
    }
    
    // 开始番茄钟会话
    startGroup();
}

