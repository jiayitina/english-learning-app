// 创建自定义单词库
function createWordLibrary(name) {
    const libraryId = 'custom_' + Date.now();
    wordLibraries[libraryId] = {
        name: name,
        words: {
            level1: []
        }
    };
    saveWordLibraries();
    updateWordLibraryList();
    alert(`已创建单词库: ${name}`);
}

// 添加单词到自定义单词库
function addWordToLibrary(libraryId, word) {
    if (!wordLibraries[libraryId]) return;
    
    if (!wordLibraries[libraryId].words.level1) {
        wordLibraries[libraryId].words.level1 = [];
    }
    
    wordLibraries[libraryId].words.level1.push(word);
    saveWordLibraries();
    alert(`已添加单词到单词库: ${wordLibraries[libraryId].name}`);
}

// 更新单词库列表
function updateWordLibraryList() {
    const wordlibList = document.getElementById('wordlib-list');
    if (!wordlibList) return;
    
    wordlibList.innerHTML = '';
    
    Object.entries(wordLibraries).forEach(([id, library]) => {
        const libraryElement = document.createElement('div');
        libraryElement.className = `wordlib-item ${id === currentWordLibrary ? 'active' : ''}`;
        libraryElement.innerHTML = `
            <span>${library.name}</span>
            <button onclick="switchWordLibrary('${id}')" class="btn">选择</button>
        `;
        wordlibList.appendChild(libraryElement);
    });
}

