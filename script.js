// 计算详细复习时间
function calculateDetailedReviewTimes() {
    if (!currentUser) return {
        easy: '无',
        normal: '无',
        medium: '无',
        hard: '无'
    };
    
    const userWordList = userWords[currentUser.username] || [];
    const now = Date.now();
    
    // 按难度分组
    const wordsByDifficulty = {
        easy: [],
        normal: [],
        medium: [],
        hard: []
    };
    
    userWordList.forEach(item => {
        if (item.nextReviewTime && item.nextReviewTime > now) {
            // 根据记忆难度分配到不同组
            if (item.memoryDifficulty <= 1) {
                wordsByDifficulty.easy.push(item);
            } else if (item.memoryDifficulty <= 2) {
                wordsByDifficulty.normal.push(item);
            } else if (item.memoryDifficulty <= 3) {
                wordsByDifficulty.medium.push(item);
            } else {
                wordsByDifficulty.hard.push(item);
            }
        }
    });
    
    // 计算每组的最早复习时间
    const result = {};
    for (const [difficulty, words] of Object.entries(wordsByDifficulty)) {
        if (words.length > 0) {
            const nextTime = Math.min(...words.map(item => item.nextReviewTime));
            const date = new Date(nextTime);
            result[difficulty] = date.toLocaleString('zh-CN', {
                year: 'numeric',
                month: '2-digit',
                day: '2-digit',
                hour: '2-digit',
                minute: '2-digit'
            });
        } else {
            result[difficulty] = '无';
        }
    }
    
    return result;
}

