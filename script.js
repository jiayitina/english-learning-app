// 计算需要复习的单词数量
function calculateReviewCount() {
    if (!currentUser) return 0;
    
    const userWordList = userWords[currentUser.username] || [];
    const now = Date.now();
    let count = 0;
    
    userWordList.forEach(item => {
        if (item.nextReviewTime && item.nextReviewTime <= now) {
            count++;
        }
    });
    
    return count;
}

// 计算下次复习时间
function calculateNextReviewTime() {
    if (!currentUser) return '无';
    
    const userWordList = userWords[currentUser.username] || [];
    const now = Date.now();
    let nextTime = null;
    
    userWordList.forEach(item => {
        if (item.nextReviewTime && item.nextReviewTime > now) {
            if (!nextTime || item.nextReviewTime < nextTime) {
                nextTime = item.nextReviewTime;
            }
        }
    });
    
    if (!nextTime) return '无';
    
    const date = new Date(nextTime);
    return date.toLocaleTimeString();
}

